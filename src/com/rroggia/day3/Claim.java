package com.rroggia.day3;

public class Claim {

	private int id;
	private int distanceLeftEdge;
	private int distanceTopEdge;
	private int width;
	private int height;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getDistanceLeftEdge() {
		return distanceLeftEdge;
	}

	public void setDistanceLeftEdge(int distanceLeftEdge) {
		this.distanceLeftEdge = distanceLeftEdge;
	}

	public int getDistanceTopEdge() {
		return distanceTopEdge;
	}

	public void setDistanceTopEdge(int distanceTopEdge) {
		this.distanceTopEdge = distanceTopEdge;
	}

	public int getWidth() {
		return width;
	}

	public void setWidth(int width) {
		this.width = width;
	}

	public int getHeight() {
		return height;
	}

	public void setHeight(int height) {
		this.height = height;
	}

}
